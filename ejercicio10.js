// 10.-   Crear un programa que le pida al usuario dos números en un Prompt 
//        y luego muestre en un alerta el número mayor.

alert("Ingrese 2 número enteros positivos");
let num1 = prompt("Ingrese el primer número");
let num2 = prompt("Ingrese el segundo número");

if (num1 > num2){
  alert("el numero mayor es: " + num1);
}else {
  alert("el numero mayor es: " + num2);
}

